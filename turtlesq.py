import turtle


hardik = turtle.Turtle()

hardik.color("blue","cyan")

hardik.begin_fill()
hardik.forward(100)
hardik.left(90)
hardik.forward(100)
hardik.left(90)
hardik.forward(100)
hardik.left(90)
hardik.forward(100)

hardik.penup()
hardik.forward(150)
hardik.pendown()
hardik.forward(100)
hardik.left(90)
hardik.forward(100)
hardik.left(90)
hardik.forward(100)
hardik.left(90)
hardik.forward(100)

hardik.end_fill()


turtle.done()

